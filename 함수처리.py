#abs 절대값 구하기
print(abs(-5)) #-5
#pow 제곱근
print(pow(4,3)) # 4의 3 승 = 64
#max 최대값
print(max(5*5, 10*110)) #10*110=1100으로 최대값이됨
#min 최소값
print(min(5*1,10/3)) #10/3 이 최소값으로 나옴
# round 반올림
print(round(10/3)) #반올림해서 3
print(round(3.99)) #반올림하면 4

from math import * # 나중에배울것 math 라이브러리를 사용한다는 뜻
print(floor(4.999)) # 내림 4
print(ceil(3.1)) # 올림 4
print(sqrt(25)) # 제곱근 5